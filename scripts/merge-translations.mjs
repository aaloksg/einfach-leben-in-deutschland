// Merges the parallel-translated scripts/output/en-batch-*.json files into
// the final src/locales/en.ts, validating all 300 question keys are present.
import fs from 'node:fs'
import path from 'node:path'
import { fileURLToPath } from 'node:url'

const __dirname = path.dirname(fileURLToPath(import.meta.url))
const root = path.resolve(__dirname, '..')
const OUT_PATH = path.join(root, 'src/locales/en.ts')

const merged = {}
for (let i = 1; i <= 6; i++) {
  const batchPath = path.join(__dirname, `output/en-batch-${i}.json`)
  const batch = JSON.parse(fs.readFileSync(batchPath, 'utf8'))
  Object.assign(merged, batch)
}

const missing = []
for (let i = 1; i <= 300; i++) {
  const key = `question_${i}`
  if (!merged[key]) missing.push(key)
  else {
    for (const field of ['question', 'answer_a', 'answer_b', 'answer_c', 'answer_d']) {
      if (!merged[key][field] || typeof merged[key][field] !== 'string') {
        missing.push(`${key}.${field}`)
      }
    }
  }
}
if (missing.length) {
  throw new Error(`Missing/invalid keys in merged translations: ${missing.join(', ')}`)
}

const ordered = {}
for (let i = 1; i <= 300; i++) ordered[`question_${i}`] = merged[`question_${i}`]

fs.writeFileSync(
  OUT_PATH,
  `// Auto-generated by scripts/merge-translations.mjs from scripts/output/en-batch-*.json.\n` +
    `export default {\n  questions: ${JSON.stringify(ordered, null, 2).replace(/\n/g, '\n  ')},\n}\n`,
)

console.log(`Merged ${Object.keys(ordered).length} questions into src/locales/en.ts`)
